    private void jButtonsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsubmitActionPerformed
        // TODO add your handling code here:
          if(flag==1)
        {
            String patientId=jTextFieldpatient.getText();
            String symptoms=jTextFieldsymptoms.getText();
           String diagnosis=jTextFieldDiagnosis.getText();
            String medicines=jTextfieldmedicines.getText();

         String wardReq;
            String typeWard;
            if(jCheckBox1.isSelected()){
                wardReq = "Yes";
                typeWard = (String)jComboBox1.getSelectedItem();
        }
              else{
                wardReq = "No";
                typeWard = "";
            }
           try{
            Connection con = ConnectionProvider.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into diagnosis values('"+ patientId + "', '" + symptoms +"', '"+ diagnosis +"', '"+ medicines +"', '"+ wardReq +"', '"+ typeWard + "')");
            JOptionPane.showMessageDialog(null, "Successfully Added");
                setVisible(false);
               new AddDiagnosis().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }                                             

    }//GEN-LAST:event_jButtonsubmitActionPerformed

